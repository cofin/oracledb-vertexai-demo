CREATE TABLE product (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    name VARCHAR2(255) NOT NULL,
    description VARCHAR2(4000),
    price NUMBER(10, 2),
    category VARCHAR2(100),
    sku VARCHAR2(100) UNIQUE,
    in_stock BOOLEAN DEFAULT TRUE
        ANNOTATIONS (
            Display 'Global stock fallback',
            Data_Role 'availability'
        ),
    metadata JSON,
    -- docs:start-schema-annotations
    embedding VECTOR(3072, FLOAT32)
        ANNOTATIONS (
            Display 'Product embedding',
            Embedding_Model 'gemini-embedding-2',
            Embedding_Dimensions '3072',
            Embedding_Purpose 'document',
            Distance 'COSINE'
        ),
    -- docs:end-schema-annotations
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT ON NULL FOR INSERT AND UPDATE CURRENT_TIMESTAMP NOT NULL
) INMEMORY PRIORITY HIGH
ANNOTATIONS (
    Display 'Cymbal Coffee products',
    Description 'Menu catalog rows with Vertex embeddings for semantic search',
    AI_Surface 'PRODUCT_RAG'
);

COMMENT ON TABLE product IS 'Products with vector embeddings for semantic search';
COMMENT ON COLUMN product.embedding IS '3072-dimensional gemini-embedding-2 vector';
COMMENT ON COLUMN product.in_stock IS 'Boolean: true=in stock, false=out of stock';


