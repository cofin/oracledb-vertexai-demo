-- Store-level product inventory for grounded availability answers
CREATE TABLE store_product_inventory (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    store_id NUMBER NOT NULL,
    product_id NUMBER NOT NULL,
    quantity_available NUMBER(10) DEFAULT 0 NOT NULL
        ANNOTATIONS (
            Display 'Pickup quantity',
            Data_Role 'availability'
        ),
    stock_status VARCHAR2(20) NOT NULL
        ANNOTATIONS (
            Display 'Stock status',
            Enum_Values 'IN_STOCK, LOW_STOCK, OUT_OF_STOCK'
        ),
    pickup_available BOOLEAN DEFAULT TRUE NOT NULL
        ANNOTATIONS (
            Display 'Pickup available',
            Data_Role 'availability'
        ),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT ON NULL FOR INSERT AND UPDATE CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT store_product_inventory_store_fk FOREIGN KEY (store_id) REFERENCES store (id) ON DELETE CASCADE,
    CONSTRAINT store_product_inventory_product_fk FOREIGN KEY (product_id) REFERENCES product (id) ON DELETE CASCADE,
    CONSTRAINT store_product_inventory_uk UNIQUE (store_id, product_id),
    CONSTRAINT store_product_inventory_status_ck CHECK (stock_status IN ('IN_STOCK', 'LOW_STOCK', 'OUT_OF_STOCK')),
    CONSTRAINT store_product_inventory_qty_ck CHECK (quantity_available >= 0)
)
ANNOTATIONS (
    Display 'Store product inventory',
    Description 'Curated store-level availability for deterministic chat grounding'
);

COMMENT ON TABLE store_product_inventory IS 'Curated store-level product availability for deterministic chat grounding';
COMMENT ON COLUMN store_product_inventory.quantity_available IS 'Current known pickup quantity for this product at this store';
COMMENT ON COLUMN store_product_inventory.stock_status IS 'Inventory status enum: IN_STOCK, LOW_STOCK, or OUT_OF_STOCK';
COMMENT ON COLUMN store_product_inventory.pickup_available IS 'Whether this product can currently be reserved for pickup at this store';


